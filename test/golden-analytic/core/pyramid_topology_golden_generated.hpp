// Closed-form pyramid topology golden constants, generated once from
// ComputeClosedFormPyramid (src/core/geo3d_closedform.*) itself.
//
// Rationale: after the closed-form rewrite the pyramid topology
// (vertex count / which faces are present / which solver branches are walked)
// is a CONSTANT of the parametrization, not a numerically-discovered quantity.
// These tests therefore assert cf's topology against a stored snapshot of cf's
// own output — a pure integer/bitmask equality with zero platform surface,
// rather than adjudicating a fragile exact-arithmetic witness. See
// doc/numerical-robustness.md §8 for why a free-symbol exact oracle was
// retired in favor of this contract.
//
// Each entry is (vtx_cnt, face_present_mask, path_tag_union) for the
// index-parallel sample in closed_form_samples_generated.hpp:
//   - vtx_cnt         : ClosedFormPyramidResult::vtx_cnt
//   - face_present_mask: bit i set iff face_present[i] (20 slots, low bit = slot 0)
//   - path_tag_union  : ClosedFormPyramidResult::path_tag_union (ClosedFormHexPathTag bits)
//
// Contract: index-for-index parallel to the sample-pool arrays in
// closed_form_samples_generated.hpp (kPyramidWellConditionedSamples,
// kPyramidMillerSamples, six kPyramidFlatTailAlpha* pools). The degenerate
// pools (kPyramidDegenerateSigma030/050Samples) are deliberately NOT snapshotted
// here — they sit on production's merge-tolerance boundary by construction, so a
// precise-match contract would reintroduce the very platform sensitivity this
// change removes; they are covered by the weak DegenerateContractSafe assertion
// instead.
//
// Generation mechanism: a throwaway TEST (ClosedFormPyramid.DumpTopologyGolden)
// walked the current sample pools, called ComputeClosedFormPyramid per sample,
// and printed these array literals to stderr. The generator is kept out of the
// tree — same convention as closed_form_samples_generated.hpp ("throwaway
// generator, replace the arrays verbatim on regeneration, never edit in place").
//
// Regeneration checklist (trigger: any sample-pool array in
// closed_form_samples_generated.hpp is added/removed/reordered):
//   1. Re-add the DumpTopologyGolden TEST (see task history / git blame of
//      test_closed_form_pyramid.cpp) walking the CURRENT sample pools.
//   2. Build golden_analytic_test and run
//      --gtest_filter=ClosedFormPyramid.DumpTopologyGolden.
//   3. Regenerate this file wholesale from the new dump. Do NOT patch
//      individual entries — the whole file is replaced or nothing.
//   4. Remove the temporary TEST again.

